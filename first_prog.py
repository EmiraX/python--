# Целочисленная арифметика, часть 1

a = 7
b = 3

print("Сложение:", a + b)
print("Вычитание:", a - b)
print("Умножение:", a * b)
print("Целочисленное деление:", a // b)
print("Остаток от деления:", a % b)
# Целочисленная арифметика, часть 2 — ввод пользователя

x = int(input("Введите первое число: "))
y = int(input("Введите второе число: "))

print(f"{x} + {y} = {x + y}")
print(f"{x} * {y} = {x * y}")
print(f"{x} делить на {y} целочисленно: {x // y}, остаток: {x % y}")
